const contractAddress = "0xc2efA79Fff659130D1ef28067670eb1ed970662c";

const Form = () => {
  const [file, setFile] = useState(null); // For the file input
  const [imgURL, setImgURL] = useState(null); // For the image URL
  const [title, setTitle] = useState("");
  const [contract, setContract] = useState(null);

  useEffect(() => {
    const init = async () => {
      if (window.ethereum) {
        await window.ethereum.request({ method: "eth_requestAccounts" });
        web3 = new Web3(window.ethereum);
        const contract = new web3.eth.Contract(abi, contractAddress);
        setContract(contract);
      } else {
        console.error("Ethereum provider not found");
      }
    };

    init();
  }, []);

  const mintNFT = async (file) => {
    const formData = new FormData();
    formData.append("file", file);

    // Upload to Pinata
    let imgHash;
    try {
      const resFile = await axios.post(
        "https://api.pinata.cloud/pinning/pinFileToIPFS",
        formData,
        {
          headers: {
            "Content-Type": "multipart/form-data",
            pinata_api_key: "ff6d3ff7737e8627277c",
            pinata_secret_api_key:
              "462bb62848846981d6d23bcf21e527172d0d2d8bde7a71047d2c4b383dc88207",
          },
        }
      );
      imgHash = `ipfs://${resFile.data.IpfsHash}`;
    } catch (error) {
      console.error("Pinata upload failed:", error);
      return;
    }

    // Mint NFT
    try {
      const accounts = await web3.eth.getAccounts();
      await contract.methods
        .mintNFT(accounts[0], imgHash)
        .send({ from: accounts[0] });
      console.log(`Minted successfully`);
    } catch (error) {
      console.error("Minting failed", error);
    }
  };

  const handleFileChange = (e) => {
    const selectedFile = e.target.files[0];
    setFile(selectedFile);

    // Create a blob URL for the file
    const objectURL = URL.createObjectURL(selectedFile);

    // Update the image URL state variable
    setImgURL(objectURL);
  };
  const handleSubmit = async (e) => {
    e.preventDefault();
    if (file) {
      const formData = new FormData();
      formData.append("file", file);
      try {
        await axios.post("http://localhost:3000/api/mint", formData, {
          headers: {
            "Content-Type": "multipart/form-data",
          },
        });
      } catch (error) {
        console.log("Axios Error: ", error);
      }
    }
  };

  return (
    <>
      <Header />
      <div className="nftbg">
        <div className="newnft">
          <p className="p1">CREATE</p>
          <p className="p2"> &nbsp;NEW NFT</p>
        </div>
        <form onSubmit={handleSubmit}>
          <section>
            <label htmlFor="file">Import file</label>
            <input
              type="file"
              name="file"
              id="file"
              onChange={handleFileChange}
            />
            <div className="photo_container">
              <img src={imgURL} alt="profile picture preview" />
            </div>
          </section>
          <section>
            <label htmlFor="title">NFT title</label>
            <input
              type="text"
              name="title"
              id="title"
              onChange={(e) => setTitle(e.target.value)}
            />
          </section>
        </form>
        <div className="bar">
          <p>Additional NFT’s details</p>
          <div className="arrow"> </div>
        </div>
        <div className="btn_NFT" onClick={handleSubmit}>
          <p className="btn_text_NFT">✨ Create Now ✨</p>
        </div>
      </div>
    </>
  );
};

export default Form;
